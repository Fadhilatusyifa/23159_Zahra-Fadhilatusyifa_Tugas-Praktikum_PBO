public class KonversiSuhu {
    
    // konversi celcius ke fahrenheit
    public double celciusTofahrenheit(double celcius){
        return (celcius * 9/5) + 32;
    }
    
    // konversi celsius ke reamur
    public double celsiusToreamur(double celcius){
        return celcius * 4/5;
    }
    
}
